def exchange(n, S):
    for i in range(1, n):
        for j in range(i+1, n+1):
            if S[i] > S[j]:
                S[i], S[j] = S[j], S[i] # swap
                print(S)

S = [-1, 10, 7, 11, 5, 13, 8]
print(S)
# [-1, 10, 7, 11, 5, 13, 8]
exchange(len(S)-1, S)
print(S)
# [-1, 5, 7, 8, 10, 11, 13]

